public class Main {
    public static void main(String[] args) {
        // Создаем переменную с любимым временем года
        Season любимоеВремяГода = Season.Лето;

        // Выводим информацию о любимом времени года
        System.out.println("Мое любимое время года: " + любимоеВремяГода);
        System.out.println("Средняя температура: " + любимоеВремяГода.getСредняяТемпература() + " градусов Цельсия");
        System.out.println("Описание: " + любимоеВремяГода.getDescription());

        // Выводим все времена года, их среднюю температуру и описание
        for (Season season : Season.values()) {
            System.out.println(season);
            System.out.println("Средняя температура: " + season.getСредняяТемпература() + " градусов Цельсия");
            System.out.println("Описание: " + season.getDescription());
        }
    }
}
// Создаем перечисление времен года с переменными для средней температуры
enum Season {
    Весна(15),
    Лето(25),
    Осень(10),
    Зима(0);

    private int средняяТемпература;

    // Конструктор для установки средней температуры
    Season(int средняяТемпература) {
        this.средняяТемпература = средняяТемпература;
    }

    // Метод для получения средней температуры
    public int getСредняяТемпература() {
        return средняяТемпература;
    }

    // Метод для получения описания времени года
    public String getDescription() {
        if (this == Лето) {
            return "Теплое время года";
        } else {
            return "Холодное время года";
        }
    }
}
